def finite_circle(size: int) -> Circle:
    """Return the finite cyclic address space with ``size`` nodes."""
    return Circle(size)


def finite_orbit(size: int, stride: int, start: int = 0) -> list[int]:
    """Return the orbit generated by repeatedly adding ``stride`` modulo ``size``."""
    return Circle(size).orbit(start, stride)


def finite_period(size: int, stride: int) -> int:
    """Return the period of ``stride`` on the finite circle of ``size`` nodes."""
    return Circle(size).period(stride)


def finite_orbit_decomposition(size: int, stride: int) -> list[list[int]]:
    """Return all stride orbits on the finite circle of ``size`` nodes."""
    return Circle(size).orbit_decomposition(stride)


def is_full_coil(size: int, stride: int) -> bool:
    """Return whether ``stride`` visits every node before closing."""
    return Circle(size).is_full_coil(stride)


